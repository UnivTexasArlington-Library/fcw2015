			<h2>About</h2>
			<p>
			The University of Texas at Arlington Libraries and the Office of the Provost are pleased to host the sixth Celebration of Faculty Creative Works, an exhibition showcasing the books, art, music, media, patents, conference activities, and journal contributions made by UT Arlington faculty in 2013.</p>
			<p>The event was initiated by the Libraries to give special attention to the depth and breadth of scholarship and creativity that takes place on campus and to strengthen ties within the campus community. It complements the University’s tradition of sharing information about faculty expertise across disciplines, which is exemplified by the Libraries-sponsored Focus on Faculty Speakers’ Series that began in 2000. As the intellectual and creative center of campus, the Libraries are an appropriate partner to promote new information and new knowledge.</p>
			<p>Expanding the boundaries of knowledge is the stock-in-trade of a research university. We want to acknowledge the productivity and distinction of our faculty and honor their accomplishments.</p>
			<p>We congratulate the individuals represented in these pages and in the exhibit. The work is diverse, compelling, and groundbreaking&mdash;things for which all of us, as UT Arlington Mavericks, can be proud.</p>
			<p class="signature"><i>~Ronald L. Elsenbaumer, Provost and Vice President for Academic Affairs<br />
			~Becca Bichel, Dean of the Library</i></p>
			<br/>
			<h2>Acknowledgments</h2>
			<p>We acknowledge with thanks the Office of the Provost and the Library for making this event possible.</p>
			<p>We recognize the following University individuals for their contributions: Nadine Douditski and Connie Frazier, Office of the Provost (mailing lists and invitation distribution), Duy Nguyen, Center for Online Development (Mentis data) and Arul Amudha Thirumaran, Office of Research Administration (patents). Members of the Library’s Faculty Creative Works 2013 Event Team are: Jody Bailey, Evelyn Barker (Chair), Suzanne Beckett, Jeff Downing, Kathleen Houston, Carolyn Kadri, Candy McCormic, Antoinette Nelson, William Oglesby, Erin O’Malley, Sunday Phillips, Donald Quarles and Eli Zoller.</p>
			<p>We are also grateful to the Faculty of UT Arlington, along with academic department staff, for contributing information for the creation of this book and the accompanying web site.  Content was drawn from Mentis Profiles and supplemented with information from department administrators. While every effort was made to be comprehensive, we apologize for any errors or omissions. 
