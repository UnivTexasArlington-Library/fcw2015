			<h2>About</h2>
			<p>
The University of Texas at Arlington Libraries and the Office of the Provost are pleased to host the seventh Celebration of Faculty Creative Works, an exhibition showcasing the books, art, music, media, patents, conference activities, and journal contributions made by UT Arlington faculty in 2014.</p>

<p>The Libraries initiated the event to give special attention to the depth and breadth of scholarship and creativity produced by UTA faculty. It complements the University’s vision of creating global impact through creative scholarship, transformative access, and collaborative learning.</p>

<p>UTA Libraries are committed to sharing, preserving, and celebrating the accomplishments of our faculty. We congratulate the individuals represented in these pages and on the Faculty Creative Works website at <a href="index.php"> library.uta.edu/fcw</a>. Their work shows the high standards of excellence for which UTA is known.</p>

			<p class="signature"><i>~Ronald L. Elsenbaumer, Provost and Vice President for Academic Affairs<br />
			~Becca Bichel, Dean of the Library</i></p>
			<br/>
			<h2>Acknowledgments</h2>
			<p>We acknowledge with thanks the University of Texas at Arlington Office of the Provost and the Libraries for making this event possible.</p>

<p>We recognize the following University individuals for their contributions: Connie Frazier and Penny Driessner, Office of the Provost; Ethan Sarp, Center for Online Development; and Teri Schultz, Office of Technology Management.

<p>Members of the Libraries’ Faculty Creative Works 2014 event team are: Evelyn Barker, Mark Cook,  Ginger Dickens, Ava Nell Harris, Kathleen Houston, Upendra Madam, Shridhar Manvi, Donald Quarles, and C.D. Walter.</p>

<p>We are also grateful to the faculty of UT Arlington, along with academic department staff, for contributing information for the creation of this book and the accompanying web site. Content was drawn from Mentis profiles and supplemented with information from department administrators. While every effort was made to be comprehensive, we apologize for any errors or omissions.</p>

